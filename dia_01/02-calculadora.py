# %% SOMA
print("1 + 1 = ", 1 + 1)

# %% SUBTRAÇÃO
print("10 - 5 = ", 10 - 5)

# %% MULTIPLICAÇÃO
print("10 * 5 = ", 10 * 5)

# DIVISÃO
# %% Número Real
print("10 / 5 = ", 10 / 5)
# %% Inteira Parte
print("10 // 5 = ", 10 // 5)
# %% Resto da Divisão
print("10 % 4 = ", 10 % 4)

# %% POTÊNCIA
print("3 ** 4 = ", 3 ** 4)